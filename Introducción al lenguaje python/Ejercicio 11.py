# Pide al usuario dos números
numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))

# Calcula la "distancia" entre los dos números
distancia = abs(numero1 - numero2)

# Muestra la distancia en la pantalla
print("La distancia entre", numero1, "y", numero2, "es:", distancia)